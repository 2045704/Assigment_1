def codons():
    frequencies = {}
    for codon, amminoacid in codon_dict.items():
        if amminoacid != 'Stop':
            if amminoacid not in frequencies:
                frequencies[amminoacid] = 0
            frequencies[amminoacid] += 1
    frequencies['Stop'] = 3
    return frequencies

def MRNA(protein):
    codon_freq = codons()
    lines = codon_freq['Stop']
    for codon in protein:
        lines *= codon_freq[codon]
    return lines
 

